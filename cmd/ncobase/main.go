// gracefulShutdown gracefully shuts down the server
func gracefulShutdown(srv *http.Server, errChan chan error) error {
	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)

	select {
	case err := <-errChan:
		return fmt.Errorf("server error: %w", err)

	case <-quit:
		ctx, cancel := context.WithTimeout(context.Background(), shutdownTimeout)
		defer cancel()

		// Execute shutdown logic
		if err := srv.Shutdown(ctx); err != nil {
			logger.Errorf(context.Background(), "Shutdown error: %v", err)
			return fmt.Errorf("shutdown error: %w", err)
		}

		// wait for server to shutdown
		<-ctx.Done()
		if errors.Is(ctx.Err(), context.DeadlineExceeded) {
			logger.Warnf(context.Background(), "Shutdown timed out after %s", shutdownTimeout)
		} else {
			logger.Infof(context.Background(), "Shutdown completed within %s", shutdownTimeout)
		}

		return nil
	}
}
